    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String codigoProduto = this.codProduto.getText();
        int quantidadeProduto = Integer.parseInt(this.qtdProduto.getText());

        Produto p = Controlador.Produto.buscarCodigo(this.emp.getProdutos(), codigoProduto);
        if (p != null) {
            DefaultTableModel model = (DefaultTableModel) this.tableProdutos.getModel();
            this.tableProdutos.setModel(model);
            model.addRow(new Object[]{p.getCodigo(), p.getDescricao(), quantidadeProduto, p.getValor()});
        } else {
            JOptionPane.showMessageDialog(null, "Produto não encontrado", "Alerta", JOptionPane.ERROR_MESSAGE);
        }

        this.codProduto.setText("");
        this.qtdProduto.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if ((!this.cpf.getText().equals("")) && this.tableProdutos.getRowCount() > 0) {
            String pag = (String) this.tipoPagamento.getSelectedItem();
            TipoPagamento p = Controlador.TipoPagamento.cadPagamento(pag, 
                                                                                                                                               this.pgtTitular.getText(), 
                                                                                                                                               this.numCard.getText());
            Venda v = new Venda(emp.getContv() + "NF", 
                                                        this.tableProdutos.getRowCount(), 
                                                        Cliente.buscarCpf(emp.getClientes(), this.cpf.getText()), 
                                                        p);
            Item[] itens = new Item[this.tableProdutos.getRowCount()];
            for (int i = 0; i < this.tableProdutos.getRowCount(); i++) {
                itens[i] = new Item(i, Controlador.Produto.buscarCodigo(emp.getProdutos(), this.tableProdutos.getValueAt(i, 0).toString()), Integer.parseInt(this.tableProdutos.getValueAt(i, 2).toString()));
                v.addItem(itens[i]);
            }
            if (emp.addVenda(v)) {
                JOptionPane.showMessageDialog(null, "Venda realizada com Sucesso. Total R$ " + v.calcularTotal(), "Sucesso", JOptionPane.WARNING_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Venda não realizada, tente novamente!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Preencha todo os dados!", "Alerta", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

