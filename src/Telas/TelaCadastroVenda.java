    private void tipoPagamentoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_tipoPagamentoItemStateChanged
        String pgt = (String) this.tipoPagamento.getSelectedItem();
        this.numCard.setText("");
        this.pgtTitular.setText("");
        if (!pgt.equals("Dinheiro")) {
            this.pgtTitular.setEditable(true);
            this.pgtTitular.setEnabled(true);
            this.numCard.setEditable(true);
            this.numCard.setEnabled(true);
        } else {
            this.pgtTitular.setEditable(false);
            this.pgtTitular.setEnabled(false);
            this.numCard.setEditable(false);
            this.numCard.setEnabled(false);
        }
    }//GEN-LAST:event_tipoPagamentoItemStateChanged

