    private void cpfFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cpfFocusLost
        String txtCpf = this.cpf.getText();
        if (!txtCpf.equals("")) {
            Cliente cli = Controlador.Cliente.buscarCpf(emp.getClientes(), txtCpf);
            String nome = "CLIENTE NÃO ENCONTRADO";
            if (cli != null) {
                nome = cli.getNome();
            }
            this.cliNome.setText(nome);
        } else {
            this.cliNome.setText("");
        }
    }//GEN-LAST:event_cpfFocusLost

